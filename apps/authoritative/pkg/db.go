func HashPassword(password string) (string, error) {
	// GenerateFromPassword automatically generates a random salt and includes it in the resulting hash
	bytes, err := bcrypt.GenerateFromPassword([]byte(password), bcrypt.DefaultCost)
	return string(bytes), err
}

// insert user into database
//
// @param u: user to insert, must include name, email, phoneNum, and password fields (id is auto generated)
// @return error
func (db *Database) InsertUser(ctx context.Context, u User) error {
	hashedPassword, err := HashPassword(u.Password)
	if err != nil {
		return fmt.Errorf("InsertUser: %w", err)
	}

	data := map[string]interface{}{
		"name":     u.Name,
		"email":    u.Email,
		"phoneNum": u.PhoneNum,
		"password": hashedPassword,
	}

	_, _, err = db.client.From("users").Insert(data, false, "", "", "").Execute()
	if err != nil {
		return fmt.Errorf("InsertUser: %w", err)
	}
	return nil
}

