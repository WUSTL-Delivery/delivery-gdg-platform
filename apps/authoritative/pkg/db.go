// insert order into database
//
// @param o: order with info from client
// @return error
func (db *Database) CreateOrder(ctx context.Context, o Order) error {
	data := map[string]interface{}{
		"userId":          o.UserID,
		"vendorId":        o.VendorID,
		"status":          o.Status,
		"dropOffLocation": o.DropOffLocation,
	}

	//id and createdAt are auto generated by database,
	//robotId starts as null and is only updated when assigned to a robot,
	//order items are inserted separately after order creation since they require the order ID

	res, _, err := db.client.From("orders").Insert(data, false, "", "representation", "").Execute()
	if err != nil {
		return fmt.Errorf("CreateOrder: %w", err)
	}

	//retrive the order and its id
	var insertedOrders []Order
	if err := json.Unmarshal(res, &insertedOrders); err != nil {
		return fmt.Errorf("CreateOrder unmarshal: %w", err)
	}
	if len(insertedOrders) == 0 {
		return fmt.Errorf("CreateOrder: no order returned after insert")
	}

	orderID := insertedOrders[0].ID

	// Insert all items associated with the order
	for _, item := range o.OrderItems {
		item.OrderID = orderID

		err := db.AddOrderItem(ctx, item)
		if err != nil {
			return fmt.Errorf("CreateOrder: failed to add order item: %w", err)
		}
	}

	return nil
}

