// insert coordinate of some place of interest (vendor, dropoff, or waypoint)
//
// @param c: coordinate to insert
// @return error
func (db *Database) InsertCoordinate(ctx context.Context, c Coordinate) error {
	data := map[string]interface{}{
		"x":    c.X,
		"y":    c.Y,
		"name": c.Name,
		"meta": c.Meta,
		"type": c.Type,
	}

	_, _, err := db.client.From("coordinates").Insert(data, false, "", "", "").Execute()
	if err != nil {
		return fmt.Errorf("InsertCoordinate: %w", err)
	}

	return nil
}

